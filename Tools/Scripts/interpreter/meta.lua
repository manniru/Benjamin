#!/usr/bin/lua

function get_Meta(word)

	if word == "open" then
		return "1"
	elseif word == "system" then
		return "2"
	elseif word == "start" then
		return "3"
	elseif word == "fox" then
		return "4"
	elseif word == "page" then
		return "5"
	elseif word == "go" then
		return "6"
	elseif word == "sky" then
		return "7"
	elseif word == "dive" then
		return "8"
	elseif word == "music" then
		return "9"
	elseif word == "close" then
		return "10"
	elseif word == "mouse" then
		return "11"
	end

end