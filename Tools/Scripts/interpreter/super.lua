#!/usr/bin/lua
-- Meta Commands in Super Mode

function get_Super(word)

	if word == "meta" then
		return "101"
	elseif word == "colon" then
		return "102"
	elseif word == "switch" then
		return "103"
	elseif word == "kick" then
		return "104"
	elseif word == "comment" then
		return "105"
	elseif word == "copy" then
		return "106"
	elseif word == "paste" then
		return "107"
	elseif word == "side" then
		return "108"
	elseif word == "love" then
		return "109"
	elseif word == "roger" then
		return "110"
	elseif word == "touch" then
		return "111"
	elseif word == "spotify" then
		return "112"
	elseif word == "double" then
		return "113"
	elseif word == "select" then
		return "114"
	elseif word == "drag" then
		return "115"
	end

end